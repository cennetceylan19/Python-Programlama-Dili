"""
if, elif, else örneği
"""
yanit=input("Yaygın kullanılan bir otomobil markası girirniz: ")

if yanit=="Tofaş":
    print("Evet, Tofaş yaygın kullanılan bir otomobil markasıdır.")

elif yanit=="Mini Cooper":
    print("Yaa, Benim sevdiğim otomobil markası ;) .")

elif yanit=="BMW":
    print("Evet, BMW yaygın kullanılan bir otomobil markasıdır.")

else:
    print(yanit, "Markanızı bilmiyoruz.")
